async function seed() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  try {
    // Vider les événements seed existants
    await conn.execute("DELETE FROM events WHERE source = 'baymora_seed'");
    
    for (const ev of events) {
      await conn.execute(
        `INSERT INTO events (title, description, category, venue_name, venue_address, city, lat, lng, date, time_start, time_end, price, dress_code, image_url, booking_url, is_vip, is_members_only, source)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [ev.title, ev.description, ev.category, ev.venue_name, ev.venue_address, ev.city,
         ev.lat, ev.lng, ev.date, ev.time_start, ev.time_end, ev.price, ev.dress_code,
         ev.image_url, ev.booking_url, ev.is_vip ? 1 : 0, ev.is_members_only ? 1 : 0, ev.source]
      );
    }
    console.log(`✓ ${events.length} événements seedés (Bordeaux + Paris)`);
  } finally {
    await conn.end();
  }
}

seed().catch(e => { console.error(e.message); process.exit(1); });
