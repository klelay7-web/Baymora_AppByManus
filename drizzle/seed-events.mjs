// Calcul des dates relatives (vendredi prochain, samedi, dimanche, etc.)
function nextWeekday(dayOfWeek) {
  const today = new Date();
  const current = today.getDay(); // 0=dim, 1=lun, ..., 6=sam
  let diff = dayOfWeek - current;
  if (diff <= 0) diff += 7;
  const d = new Date(today);
  d.setDate(today.getDate() + diff);
  return d.toISOString().split('T')[0];
}

